# Integer value : 정수 
a = 123
b = -123
c = 0

# Float value : 실수
d = 1.2
e = -3.45

# Octal value : 8진수
f = 0o177
print(f)

# Hexadecimal value : 16진수
g = 0x8ff
h = 0xABC
print(h)

# 4칙 연산 : + - * /
i = 3
j = 4
print(i + j)
print(j - i)
print(i * j)
print(i / j)

# 제곱 : **
k = 3
l = 4
print(k ** l)

# 나머지 : %
m = 3
n = 4
print(m % n)
print(n % m)

# 몫 : //
o = 3
p = 4
print(p /  o)
print(p // o)
